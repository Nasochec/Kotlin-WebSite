CREATE TABLE BOOK (
    ID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME       VARCHAR (100) NOT NULL,
    AUTHOR_LOGIN VARCHAR(40) NOT NULL,
    CONSTRAINT FK_BOOK_AUTHOR FOREIGN KEY (AUTHOR_LOGIN) REFERENCES AUTHOR(LOGIN)
            ON DELETE CASCADE ON UPDATE CASCADE,
    CREATION_DATE DATETIME NOT NULL,
    NEEDED_AGE INTEGER NOT NULL,
    CONSTRAINT FK_BOOK_RATING FOREIGN KEY (NEEDED_AGE) REFERENCES RATING(NEEDED_AGE)
            ON DELETE CASCADE ON UPDATE CASCADE,
    NEEDED_AGE_SET_BY_ADMIN BOOLEAN NOT NULL DEFAULT FALSE,
    FORMAT_NAME VARCHAR (50) NOT NULL,
    CONSTRAINT FK_BOOK_FORMAT FOREIGN KEY (FORMAT_NAME) REFERENCES FORMAT(NAME)
            ON DELETE CASCADE ON UPDATE CASCADE,
    ANNOTATION CHARACTER LARGE OBJECT NOT NULL,
    GENRE_NAME VARCHAR (50) NOT NULL,
    CONSTRAINT FK_BOOK_GENRE FOREIGN KEY (GENRE_NAME) REFERENCES GENRE(NAME)
            ON DELETE CASCADE ON UPDATE CASCADE
);
